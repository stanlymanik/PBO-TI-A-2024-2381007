public class Aritmatika {
    public static void main(String[] args) {
        int operand1 = 20;
        int operand2 = 40;

        System.out.println(operand1 + operand2);
        System.out.println(operand1 - operand2);
        System.out.println(operand1 * operand2);
        System.out.println(operand1 / operand2);
        System.out.println(operand1 % operand2);
    }
}
