public class logika {
    public static void main(String[] args) {
        boolean a = true;
        boolean b = false;

        boolean hasilAnd = a && b;
        System.out.println("a AND b = " + hasilAnd);

        boolean hasilOr = a || b;
        System.out.println("a OR b = " + hasilOr);

    }
}
